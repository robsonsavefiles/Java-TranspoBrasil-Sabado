    public final void update() {

        SwingWorker<Void, Void> worker = new SwingWorker<Void, Void>() {
            @Override
            protected Void doInBackground() throws Exception {
                while (true) {
                    publish();
                }
            }

            @Override
            // Can safely update the GUI from this method.
            protected void process(List<Void> chunks) {
                Deposito dep = mineradora.getDeposito();
                Fonte fon = mineradora.getFonte();

                pbMinerio.setValue((int) (dep.getQtdRecurso() * 100 / dep.getQtdMaxRecurso()));

                pbMinerio.setString(Formatter.formatValor(dep.getQtdRecurso()) + "/" + Formatter.formatValor(dep.getQtdMaxRecurso()));
                textMinerio.setText(Formatter.formatValor(mineradora.getProdutividadeMaquinaMinerio())
                        + " kg/s (Max: " + Formatter.formatValor(mineradora.getProdutividadeMaquinaMinerioMax()) + " kg/s)");
                qtdMinerioRemovida.setText(Formatter.formatValor(dep.getQtdRemovida()));

                pbEnergia.setValue((int) (fon.getQtdRecurso() * 100 / fon.getQtdMaxRecurso()));

                pbEnergia.setString(Formatter.formatValor(fon.getQtdRecurso()) + "/" + Formatter.formatValor(fon.getQtdMaxRecurso()));
                textEnergia.setText(Formatter.formatValor(mineradora.getProdutividadeOperarioEnergia())
                        + " kw/s (Max: " + Formatter.formatValor(mineradora.getProdutividadeOperarioEnergia()) + " kw/s)");
                qtdEnergiaRemovida.setText(Formatter.formatValor(fon.getQtdRemovida()));
            }
        };

        worker.execute();
    }

