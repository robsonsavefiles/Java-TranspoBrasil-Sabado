    private void btnAddMaquinaGrandeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddMaquinaGrandeActionPerformed
        mineradora.addMaquina(new MaquinaGrande(mineradora.getDeposito(), mineradora.getFonte()));
        int qtd = 0;
        qtd = mineradora.getMaquinas().stream().filter((maquina) -> (maquina instanceof MaquinaGrande)).map((_item) -> 1).reduce(qtd, Integer::sum);
        qtdMg.setText(String.valueOf(qtd));
    }//GEN-LAST:event_btnAddMaquinaGrandeActionPerformed

